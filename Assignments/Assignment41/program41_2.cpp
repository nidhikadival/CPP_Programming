#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Sum
//  Description :   It returns the sum of the digits of given number recursively
//  Input :         int
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int Sum(int iNo)
{
    int iDigit = 0;
    static int iSum = 0;

    if(iNo!=0)
    {
        iDigit = iNo%10;
        iNo = iNo/10;
        iSum = iSum + iDigit;
        iSum = Sum(iNo);
    }
    return iSum;
} // End of Sum

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout<<"Enter number: \n";
    cin>>iValue;

    iRet = Sum(iValue);
    cout<<"Sum is: "<<iRet<<"\n";

    return 0;
}

