#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Factorial
//  Description :   It returns the factorial of given number recursively
//  Input :         int
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////


int Factorial(int iNo)
{
    int static iFact = 1;
    int static iCnt = 1;

    if(iCnt <= iNo)
    {
        iFact = iCnt*iFact;
        iCnt++;
        iFact = Factorial(iNo);
    }
    return iFact;
} // End of Factorial

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout<<"Enter number:\n";
    cin>>iValue;

    iRet = Factorial(iValue);
    cout<<"Factorial is "<<iRet<<"\n";

    return 0;
}

