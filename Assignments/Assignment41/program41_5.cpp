#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Mult
//  Description :   It returns the product of the digits of given number recursively
//  Input :         int
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int Mult(int iNo)
{
    int iDigit = 1;
    static int iMul = 1;

    if(iNo!=0)
    {
        iDigit = iNo%10;
        iNo = iNo/10;
        iMul = iMul * iDigit;
        iMul = Mult(iNo);
    }
    return iMul;
} // End of Mult

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout<<"Enter number: \n";
    cin>>iValue;

    iRet = Mult(iValue);
    cout<<"Product is: "<<iRet<<"\n";

    return 0;
}

