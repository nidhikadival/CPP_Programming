#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Strlen
//  Description :   It returns the string length of given string recursively
//  Input :         char *
//  Output :        int
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int Strlen(char *str)
{
    static int iLength = 0;

    if( (*str) !='\0')
    {
        iLength = iLength+1;   
        str++; 
        iLength = Strlen(str);       
    }
    return iLength;
} // End of Strlen

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    char Arr[20] = {'\0'};

    cout<<"Enter string: \n";
    cin>>Arr;

    iRet = Strlen(Arr);
    cout<<"String length is "<<iRet<<"\n";

    return 0;
}

