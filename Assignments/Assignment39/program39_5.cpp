#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Display
//  Description :   It displays increasing alphabets 'a' recursively
//  Input :         -
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          2/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static char ch = 'a';

    while(ch<='f')
    {
        cout<<ch<<"\t";
        ch++;
        Display();
    }
} // End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

