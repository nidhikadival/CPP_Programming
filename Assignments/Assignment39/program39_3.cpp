#include<iostream>
using namespace std;


/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Display
//  Description :   It displays decreasing numbers according to input recursively
//  Input :         -
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          2/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 5;

    while(iCnt>=1)
    {
        cout<<iCnt<<"\t";
        iCnt--;
        Display();
    }
} // End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

