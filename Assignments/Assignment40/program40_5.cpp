#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Display
//  Description :   It displays increasing alphabets ('a') according to input recursively
//  Input :         int
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static char ch = 'a';
    char limit = (int) ('a') +iNo;
    
    if(ch < limit)
    {
        cout<<ch<<"\t";
        ch++;
        Display(iNo);
    }
} // End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout<<"Enter number: \n";
    cin>>iValue;

    Display(iValue);
    return 0;
}

