#include<iostream>
using namespace std;

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name : Display
//  Description :   It displays * pattern according to input recursively
//  Input :         int
//  Output :        void
//  Author :        Nidhi Girish Kadival
//  Date :          3/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo >= 1)
    {
        cout<<"*\t";
        Display(iNo-1);
    }
} // End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout<<"Enter number: \n";
    cin>>iValue;

    Display(iValue);
    return 0;
}

